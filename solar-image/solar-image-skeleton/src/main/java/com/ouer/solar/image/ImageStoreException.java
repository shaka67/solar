public class ImageStoreException extends Exception {

	/**
	 *
	 */
	private static final long serialVersionUID = 1L;

	public ImageStoreException() {
		super();
	}

	public ImageStoreException(String msg) {
		super(msg);
	}

	public ImageStoreException(Throwable t) {
		super(t);
	}

	public ImageStoreException(String msg, Throwable t) {
		super(msg, t);
	}

}
